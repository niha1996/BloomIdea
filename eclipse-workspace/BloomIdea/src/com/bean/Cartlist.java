package com.bean;

public class Cartlist {
	int id,uid,sid,price,nettotal;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getNettotal() {
		return nettotal;
	}

	public void setNettotal(int nettotal) {
		this.nettotal = nettotal;
	}
	

}



